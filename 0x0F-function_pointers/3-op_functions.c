#include "3-calc.h"

/**
  * op_add - addition
  * @a: parameter
  * @b: parameter
  * Return: addition
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - subtraction
  * @a: parameter
  * @b: parameter
  * Return: subsraction
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - multiplication
  * @a: parameter
  * @b: parameter
  * Return: multiplication
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - division
  * @a: parameter
  * @b: parameter
  * Return: division
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - mudulus
  * @a: parameter
  * @b: parameter
  * Return: division remainder
  */
int op_mod(int a, int b)
{
	return (a % b);
}
